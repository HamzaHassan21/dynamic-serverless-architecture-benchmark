const { DynamoDBClient } = require("@aws-sdk/client-dynamodb");
const { DynamoDBDocumentClient, ScanCommand } = require("@aws-sdk/lib-dynamodb");

const client = new DynamoDBClient({ region: "eu-west-2" });
const docClient = DynamoDBDocumentClient.from(client);

exports.handler = async (event) => {
  try {
    const params = event.queryStringParameters || {};

    const region = params.region ? params.region.toUpperCase() : null;
    const category = params.category ? params.category.toLowerCase() : null;

    const command = new ScanCommand({
      TableName: "Products"
    });

    const data = await docClient.send(command);

    let products = data.Items || [];

    if (region) {
      products = products.filter(item => item.region === region);
    }

    if (category) {
      products = products.filter(item => item.category === category);
    }

    const response = {
      message: "Dynamic product catalogue response generated by Regional AWS Lambda",
      architecture: "Regional Lambda",
      filters: {
        region: region || "ALL",
        category: category || "ALL"
      },
      count: products.length,
      results: products,
      timestamp: new Date().toISOString()
    };

    return {
      statusCode: 200,
      headers: {
        "Content-Type": "application/json",
        "Access-Control-Allow-Origin": "*"
      },
      body: JSON.stringify(response)
    };

  } catch (error) {
    console.error("Lambda error:", error);

    return {
      statusCode: 500,
      headers: {
        "Content-Type": "application/json",
        "Access-Control-Allow-Origin": "*"
      },
      body: JSON.stringify({
        message: "Internal server error",
        error: error.message
      })
    };
  }
};